// while

let i = 0
while(i < 10){
    console.log(i)
    i++
}

let myArr = ["flash", "batman", "superman"]
let arr = 0
while(arr < myArr.length){
    console.log(`value is ${myArr[arr]}`)
    arr++
}

// do-while

let score = 1
do{
    console.log(`score is ${score}`)
    score++
}
while(score <= 10)


